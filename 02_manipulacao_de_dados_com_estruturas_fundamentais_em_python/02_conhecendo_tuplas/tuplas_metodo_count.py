# count

cores = ('vermelho', 'azul', 'verde', 'azul',)

cores.count('vermelho') # 1
cores.count('azul') # 2
cores.count('verde') # 1

# tupla.count(valor) retorna o número de vezes que o valor aparece dentro da tupla.

# Tuplas são imutáveis, ou seja, você não pode adicionar ou remover elementos depois de criadas.

# Listas são mutáveis, permitindo alterações.

# Mas métodos como .count() e .index() funcionam igual em ambos os tipos.
# index

linguagens = ("python", "js", "c", "java", "csharp",)

linguagens.index("java")   # 3
linguagens.index("python") # 0


# Ele retorna o índice da primeira ocorrência do valor informado.

# Assim como o método .count(), o .index() funciona igual em tuplas e listas.

# A diferença é que, se o valor não existir, o Python gera um erro ValueError.
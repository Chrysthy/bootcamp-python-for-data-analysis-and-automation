# A forma mais comum para percorrer os dados de uma tupla é utilizando o comando for

carros = ("gol", "celta", "palio",)

for carro in carros:
    print(carro)


# A variável carros é uma tupla, ou seja, uma sequência imutável de elementos.

# O comando for carro in carros: percorre cada item da tupla, atribuindo-o à variável carro a cada iteração.

# O print(carro) exibe cada elemento na tela, um por linha.

# Esse tipo de estrutura é útil para iterar sobre coleções sem precisar usar índices manualmente.
# len

linguagens = ("python", "js", "c", "java", "csharp",)

len(linguagens) # 5

# A função len() retorna o tamanho da sequência, ou seja, quantos itens ela contém.

# No caso, a tupla linguagens possui 5 elementos, então o resultado é 5.

# 🔹 Observação:  
# O len() funciona da mesma forma para listas, strings e dicionários — sempre retornando a quantidade de itens ou caracteres.
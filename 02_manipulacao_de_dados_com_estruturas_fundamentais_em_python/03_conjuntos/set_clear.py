# clear

sorteio = {1, 23}

print(sorteio) # {1, 23}

sorteio.clear()

print(sorteio) # {}

# serve para esvaziar completamente um conjunto, removendo todos os elementos de uma vez.

# Antes do clear, o conjunto tinha {1, 23}.

# Depois do clear, o conjunto fica vazio.

# O resultado é set(), que representa um conjunto vazio (não confundir com {}, que seria um dicionário vazio).
# Keys

contatos = {
    "guilherme@gmail.com": {"nome": "Guilherme", "telefone": "3333-2221"}
}

contatos.keys()
# dict_keys(['guilherme@gmail.com'])
# Aqui, o dicionário tem apenas uma chave: "guilherme@gmail.com".
# O resultado mostra isso dentro de dict_keys.


# Retorna todas as chaves do dicionário em um objeto especial chamado dict_keys.

novo_dicionario = {"a": 100, 1: "teste", "b": "python"}
print(novo_dicionario.keys())
# dict_keys(['a', 1, 'b'])


# Saber todas as chaves que um dicionário tem
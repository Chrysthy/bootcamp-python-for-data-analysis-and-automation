# Operadores de atribuição
# defini um valor inicial ou sobrescreve o valor de uma variável

# atribuição simples
saldo = 500
print(saldo)

# atribuição com adição
saldo = 500
saldo += 200
# saldo = saldo + 200
print(saldo)

# atribuição com subtração
saldo = 500
saldo -= 200
print(saldo)

# atribuição com divisão float e inteira
saldo = 500
saldo /= 5
print(saldo)

saldo = 500
saldo //= 5
print(saldo)

# atribuição com multiplicação
saldo = 500
saldo *= 5
print(saldo)


# atribuição com módulo - resto da divisão
saldo = 500
saldo %= 480
print(saldo)

# atribuição com exponenciação
saldo = 80
saldo **= 2
print(saldo)